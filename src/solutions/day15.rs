use aoc_runner_derive::{aoc, aoc_generator};
use std::collections::BinaryHeap;
use std::collections::HashSet;
use std::cmp::Reverse;
use std::iter::repeat;

const DIRECTIONS: [(isize, isize); 4] = [
    (1,0),
    (0,1),
    (-1,0),
    (0,-1)
];

#[aoc_generator(day15)]
pub fn generator(raw_input: &str) -> Vec<Vec<u8>> {
    raw_input.lines()
        .map(|s| s.trim())
        .filter(|s| !s.is_empty())
        .map(|s| s.chars().map(|c| c.to_string().parse().unwrap()).collect::<Vec<u8>>())
        .collect()
}

 fn tile(cave: &[Vec<u8>]) -> Vec<Vec<u8>> {
    let height = cave.len();
    let width = cave[0].len();
    repeat(cave.iter())
        .take(5)
        .flatten()
        .enumerate()
        .map(|(y_ind, row)| {
            repeat(row.iter())
                .take(5)
                .flatten()
                .enumerate()
                .map (|(x_ind, r)| {
                    let next = (r + (x_ind / width) as u8 + (y_ind / height) as u8) % 9;
                    if next == 0 {
                        return 9;
                    }
                    next
                })
                .collect::<Vec<u8>>()
        })
        .collect::<Vec<Vec<u8>>>()
}


fn djikstras(cave: &[Vec<u8>]) -> usize {

    let goal = (cave.len() - 1, cave[0].len() - 1);
    let height_range = 0..cave.len();
    let width_range = 0..cave[0].len();
    let mut heap = BinaryHeap::with_capacity(cave.len() * cave[0].len());
    let mut seen = HashSet::<(usize, usize)>::with_capacity(cave.len() * cave[0].len());
    heap.push(Reverse((0, (0,0))));
    loop {
        if heap.len() < 1 {
            panic!("couldn't find a path!")
        }
        let Reverse((risk, location)) = heap.pop().unwrap();
        if seen.contains(&location) {
            continue;
        }
        seen.insert(location);
        let y = location.1 as isize;
        let x = location.0 as isize;
        if location == goal {
            return risk;
        }
        DIRECTIONS.iter()
            .filter(|(dx, dy)| {
                let in_bounds = width_range.contains(&((x + dx) as usize)) && height_range.contains(&((y + dy) as usize));
                if !in_bounds {
                    return false;
                }
                !seen.contains(&((x + dx) as usize, (y + dy) as usize))
            })
            .for_each(|(dx, dy)| {
                let next_location = ((x + dx) as usize, (y + dy) as usize);
                let next_risk = cave[next_location.1][next_location.0] as usize;
                heap.push(Reverse((risk + next_risk, next_location)))
            });
    }
}

#[aoc(day15, part1)]
pub fn solve_part1(cave: &[Vec<u8>]) -> usize {
    djikstras(cave)
}

#[aoc(day15, part2)]
pub fn solve_part2(cave: &[Vec<u8>]) -> usize {
    djikstras(&tile(cave))
}

